const month = 8
switch(month){
    case 0:
        console.log("Jan")
        break;
    case 1:
        console.log('Feb')
        break;
    case 2: 
        console.log("Mar")
        break;
    case 3:
        console.log("Apr")
        break;
    case 4:
        console.log("May")
        break;
    case 5:
        console.log("June")
        break;
    case 6:
        console.log("July")
        break;
    default:
        console.log("Some other month!")
        break;
}